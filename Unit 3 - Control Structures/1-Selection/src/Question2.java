
class BooleanVariables
{
	public static void main (String[] args)
	{
		boolean perhaps, maybe;
		perhaps = 4 < 5;
		maybe = -17 % 4 == 1;
		System.out.println("perhaps:  "+ perhaps);
		System.out.println("maybe:   " + maybe);
		//This program will print “true” as both of these two statements are true as “4 < 5” is true because 5 is greater than 4 as well as “-17 % 4 == 1” is true as -17 mod 4 is 1 and 1 equals to 1 therefore both of these Boolean statements are true.
	}
}



